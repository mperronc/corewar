#include "../incl/libft.h"

int		ft_pow(int n, int p)
{
	int i;
	int res;

	if (!p)
		return (1);
	res = n;
	i = 1;
	while (i < p)
	{
		res = res * n;
		i++;
	}
	return (res);
}
